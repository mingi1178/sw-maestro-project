"""LLM 프롬프트 정의 (한국어).

응답을 엄격한 JSON 으로 받아 백엔드에서 안전하게 파싱한다.
"""

from __future__ import annotations

SYSTEM_PROMPT = """\
당신은 시니어 프로젝트 매니저이자 회의록 분석 전문가입니다.
회의 안건과 회의 녹취록을 보고, 객관적이고 실무적으로 다음을 추출합니다:
1) 회의 요약 (3~5문장)
2) 누락된 안건 (초기 안건 중 실제로 논의되지 않은 항목)
3) 다음 회의에서 다뤄야 할 안건
4) 액션 아이템 (상위 티켓/하위 티켓, 담당자/마감일/할 일)

규칙:
- 추측을 최소화하고, 녹취록에 근거한 내용만 작성합니다.
- 담당자가 불명확하면 빈 문자열로 둡니다.
- 마감일은 가능하면 YYYY-MM-DD 형식으로, 불명확하면 빈 문자열로 둡니다.
- action_items의 각 항목은 상위 티켓입니다. 상위 티켓은 회의에서 합의된
  결과물, 책임 단위, 완료 목표를 나타냅니다.
- sub_items는 상위 티켓을 완료하기 위한 구체적인 하위 작업입니다.
  회의록에 근거가 없으면 억지로 만들지 말고 빈 배열로 둡니다.
- 하위 작업의 담당자/마감일이 상위 티켓과 같아도 명시합니다.
- 응답은 반드시 아래 JSON 스키마만 출력합니다. 다른 텍스트, 마크다운,
  코드 블록 펜스를 포함하지 않습니다.
"""

USER_PROMPT_TEMPLATE = """\
[회의 안건]
{agenda}

[회의 녹취록]
{transcript}

[출력 JSON 스키마]
{{
  "summary": "string",
  "missed_agenda": "string (각 항목은 줄바꿈 또는 1. 2. 형태)",
  "next_agenda": "string (각 항목은 줄바꿈 또는 1. 2. 형태)",
  "action_items": [
    {{
      "title": "상위 티켓 제목",
      "who": "상위 티켓 담당자 or empty",
      "when": "YYYY-MM-DD or empty",
      "what": "상위 티켓의 완료 목표/작업 설명",
      "sub_items": [
        {{"who": "string", "when": "YYYY-MM-DD or empty", "what": "하위 작업"}}
      ]
    }}
  ]
}}

위 스키마에 맞는 JSON만 출력하세요.
"""


def build_user_prompt(agenda: str, transcript: str) -> str:
    return USER_PROMPT_TEMPLATE.format(agenda=agenda.strip(), transcript=transcript.strip())


